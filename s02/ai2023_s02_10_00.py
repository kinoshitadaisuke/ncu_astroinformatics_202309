print (f'e    = {e}')

# positive infinity
pinf = math.inf
print (f'+inf = {pinf}')

# negative infinity
ninf = -math.inf
print (f'-inf = {ninf}')

# NaN (not a number)
nan = math.nan
print (f'NaN  = {nan}')
